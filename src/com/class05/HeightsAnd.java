package com.class05;

public class HeightsAnd {
	public static void main(String [] args) {
		
		int height = 6;
		
		if (height >=1 && height <5) {
			System.out.println("Short");
		}else if (height >=5 && height <6) {
			System.out.println("Medium");
		}else if (height>=6) {
			System.out.println("Tall");
		}
		
	}

}
