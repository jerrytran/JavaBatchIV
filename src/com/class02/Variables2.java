package com.class02;

public class Variables2 {

	public static void main(String[] args) {
		// different ways to create/declare variables;
		// 1. create variable and assign value
		int num = 28;

		// 2. ways: using 2 steps

		int y; // 1. declaring variable
		y = 15; // 2. assign value

		// 3. declare 1 by 1
		int a;
		int b;
		int c;
		int d;
		int f;

		// or declare all together
		int x, j, k, e, t;

		a = 1;
		b = 2;
		c = 3;
		d = 4;
		f = 5;

	}

}
