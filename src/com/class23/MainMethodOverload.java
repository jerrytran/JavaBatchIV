package com.class23;

public class MainMethodOverload {

	public static void main(String[] args) {
		System.out.println("I am main method with string argyment array");
	}
	
	public static void main() {
		System.out.println("I am main method with with no parameters");
	}
	
	public static void main(String args) {
		System.out.println("I am main method with string parameters");
	}
	
	public static void main(int a) {
		System.out.println("I am main method with int parameters");
	}
	

}
